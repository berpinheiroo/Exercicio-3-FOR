/* Utilizando o FOR, faça um algoritmo que receba 5 números e conte
quantos estão entre o intervalo dos números 1 - 10. */


var numeros
var quantidade = 0

for (var contador = 0; contador < 5; contador++) {
    numeros = parseInt(prompt("Escolha um numero"))
    if (numeros >= 1 && numero <= 10) {
        quantidade++
    }
   
}

console.log("A quantidade de numeros entre 1 e 10 e:", quantidade)





